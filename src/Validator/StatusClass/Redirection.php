class Redirection
{
    /**
     * Test if the status code falls within the range.
     *
     * @param integer $statusCode The status code to test
     * @return boolean
     */
    public function isValid($statusCode)
    {
        return (($statusCode >= 300) && ($statusCode <= 400));
    }
}
